    public void actionPerformed(ActionEvent ae){
        try{
            conn con = new conn();
            if(ae.getSource() == Search_btn){
                String sql = "select * from account where username=?";
                PreparedStatement st = con.c.prepareStatement(sql);

                st.setString(1, Username_tf.getText());
                ResultSet rs = st.executeQuery();

                while (rs.next()) {
                    Name_tf.setText(rs.getString("name"));
                    Security_tf.setText(rs.getString("sec_q"));
                }

            }
            if(ae.getSource() == Retrieve_btn){
                String sql = "select * from account where sec_ans=?";
                PreparedStatement st = con.c.prepareStatement(sql);

                st.setString(1, Answer_tf.getText());
                ResultSet rs = st.executeQuery();

                while (rs.next()) {
                    Password_tf.setText(rs.getString("password"));
                }

            }
            if(ae.getSource() == Back){
                this.setVisible(false);
                new Login_user().setVisible(true);

            }
        }catch(Exception e){

        }
    }

