    public void actionPerformed(ActionEvent ae){
        String msg = ae.getActionCommand();
        if(msg.equals("Logout")){
            setVisible(false);
            new Login_user().setVisible(true);
        }else if(msg.equals("Exit")){
            System.exit(ABORT);
        } else if(msg.equals("Books Details")){
            setVisible(false);
            new BookDetails().setVisible(true);
        }else if(msg.equals("Student Details")){
            setVisible(false);
            new StudentDetails().setVisible(true);

        }

        if(ae.getSource() == AddBooks_btn){
            this.setVisible(false);
            new AddBook().setVisible(true);
        }
        if(ae.getSource() == Statistics_btn){
            this.setVisible(false);
            new Statistics().setVisible(true);
        }
        if(ae.getSource() == AddStudents_btn){
            this.setVisible(false);
            new AddStudent().setVisible(true);
        }
        if(ae.getSource() == IssueBook_btn){
            this.setVisible(false);
            new IssueBook().setVisible(true);
        }
        if(ae.getSource() == ReturnBook_btn){
            this.setVisible(false);
            new ReturnBook().setVisible(true);

        }
        if(ae.getSource() == AboutUs_btn){
            this.setVisible(false);
            //new aboutUs().setVisible(true);

        }

    }

