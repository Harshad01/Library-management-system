    public void actionPerformed(ActionEvent ae){
        try{
            conn con = new conn();
            if(ae.getSource() == Add_btn){
                String sql = "insert into book(book_id, name, isbn, publisher, edition, price, pages) values(?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement st = con.c.prepareStatement(sql);
                // st.setInt(1, Integer.parseInt(textField.getText()));
                st.setString(1, BookID_tf.getText());
                st.setString(2, Name_tf.getText());
                st.setString(3, ISBN_tf.getText());
                st.setString(4, Publisher_tf.getText());
                st.setString(5, (String) Edition_Jcombo.getSelectedItem());
                st.setString(6, Price_tf.getText());
                st.setString(7, Pages_tf.getText());

                int rs = st.executeUpdate();
                if (rs > 0)
                    JOptionPane.showMessageDialog(null, "Successfully Added");
                else
                    JOptionPane.showMessageDialog(null, "Error");
                BookID_tf.setText("");
                Name_tf.setText("");
                ISBN_tf.setText("");
                Publisher_tf.setText("");
                Price_tf.setText("");
                Pages_tf.setText("");
                st.close();
            }
            if(ae.getSource() == Back_btn){
                this.setVisible(false);
                new Home().setVisible(true);

            }
            con.c.close();
        }catch(Exception e){

        }
    }

